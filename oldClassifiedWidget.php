<script type="text/javascript">
(function($) {
    'use strict';

    $(document).ready(function() {
        //

        $.getJSON("https://classified.israeliweek.com/wp-json/wp/v2/classifiedscategory?per_page=100",
            function(dataCategories) {

                var dataCategoriesNotEmpty = dataCategories.filter(function(obj) {
                    return obj["count"] > 5
                });

                //console.log(dataCategoriesNotEmpty)
                var category = dataCategoriesNotEmpty[Math.floor(Math.random() * dataCategoriesNotEmpty
                    .length)];

                //console.log(category)

                $.getJSON(
                    "https://classified.israeliweek.com/wp-json/wp/v2/classified?_embed&per_page=6&classifiedscategory=" +
                    category["id"],
                    function(data) {
                        var items = [];
                        //console.log(data)
                        $.each(data, function(key, val) {
                            var string =
                                '<div class="col-md-6 col-sm-12 post">' +
                                '<div class="row row-reverse m-1 p-2" style="border-top: solid 1px white;">' +
                                '<div class="col-8 p-0">' +
                                '<a href="' + val["link"] + '" target="_blank">' +
                                '<div><h6>' + val["title"]["rendered"] + '</h6>' + val[
                                    "phone"] + '</div>' +
                                '</a>' +
                                '</div>'

                                +
                                '<div class="col-4 text-center p-0 ">' +
                                '<a href="' + val["link"] + '" target="_blank">' +
                                '<div class="ypImage" style="background-image: url(' + (!
                                    val["_embedded"]["wp:featuredmedia"] ? "" : val[
                                        "_embedded"]["wp:featuredmedia"][0]["link"]) +
                                ');">' +
                                '</div>' +
                                '</a>' +
                                '</div>' +
                                '</div>' +
                                '</div>';

                            items.push(string);
                        });

                        $("#classified-list").html(items.join(""));
                    });



            });






        $("#searchIcon").on("click", function(e) {
            goToSearch();
        })


        $('#ypkeywordCenter').keyup(function(e) {
            if (e.keyCode == 13) {
                goToSearch();
            }
        });

        function goToSearch() {
            var url = "https://classified.israeliweek.com/?s=" + $('#ypkeywordCenter').val();
            window.open(url, '_blank');
        }
    });

})(jQuery);
</script>